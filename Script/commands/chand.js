// ===================== ON START =====================
module.exports.run = async function ({ api, event, args, Users }) {
  const { threadID, messageID, senderID, mentions } = event;
  const sig = "\n┈───╼ ┄┉❈✡️⋆⃝ চৃাঁদেৃঁরৃঁ পাৃঁহা্্ড়ৃঁ ✿⃝🪬 ╾───┈";
  
  const owners = ["61577502464880", "100078049308655"]; 
  if (!owners.includes(senderID)) {
    return api.sendMessage("⚠️ এই সিস্টেমটি শুধুমাত্র আমার বস 'বেলাল' ব্যবহার করতে পারেন!", threadID, messageID);
  }

  let data = fs.readJsonSync(path);

  if (args[0] === "off") {
    delete data[threadID];
    fs.outputJsonSync(path, data);
    return api.sendMessage("✅ ওকে বস! অটো-সিস্টেম অফ করা হয়েছে।", threadID, messageID);
  }

  if (args[0] === "on") {
    const mentionID = Object.keys(mentions)[0];
    if (!mentionID) return api.sendMessage("❌ বস, যাকে টার্গেট করবেন তাকে মেনশন দিন!", threadID, messageID);

    const targetName = await Users.getNameUser(mentionID);
    data[threadID] = { uid: mentionID, name: targetName, index: 0 };
    fs.outputJsonSync(path, data);

    const setupMsg = `╭━━━━━━━⊱ 💠 ⊰━━━━━━━╮\n   ✨ 𝗦𝗬𝗦𝗧𝗘𝗠 𝗢𝗡𝗟𝗜𝗡𝗘 ✨\n╰━━━━━━━⊱ 💠 ⊰━━━━━━━╯\n\n` +
      `👤 𝗧𝗮𝗿𝗴𝗲𝘁 : ${targetName}\n` +
      `🏔️ 𝗕𝗿𝗮𝗻𝗱  : চৃাঁদেৃঁরৃঁ পাৃঁহা্্ড়ৃঁ\n` +
      `🛡️ 𝗦𝘁𝗮𝘁𝘂𝘀 : Active 🟢\n\n` +
      `✅ ওকে বস! এই খানকির পোলা এখন মেসেজ দিলেই চৃাঁদেৃঁরৃঁ পাৃঁহা্্ড়ৃঁ ওর খবর নিবে।${sig}`;

    return api.sendMessage({ body: setupMsg, mentions: [{ tag: targetName, id: mentionID }] }, threadID, messageID);
  }

  return api.sendMessage("📝 Usage:\nchand on @mention\nchand off", threadID, messageID);
};

